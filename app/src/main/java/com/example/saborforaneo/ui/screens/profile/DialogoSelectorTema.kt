@Composable
fun DialogoSelectorTema(
    temaActual: TemaColor,
    alCerrar: () -> Unit,
    alSeleccionar: (TemaColor) -> Unit
) {
    var temaSeleccionado by remember { mutableStateOf(temaActual) }

    AlertDialog(
        onDismissRequest = alCerrar,
        icon = {
            Icon(
                imageVector = Icons.Default.Palette,
                contentDescription = null
            )
        },
        title = { Text("Tema de Color") },
        text = {
            Column(
                modifier = Modifier.fillMaxWidth()
            ) {
                Text(
                    text = "Elige tu color favorito para personalizar la aplicación:",
                    fontSize = 14.sp,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.7f),
                    modifier = Modifier.padding(bottom = 16.dp)
                )

                TemaColor.values().forEach { tema ->
                    ItemTema(
                        tema = tema,
                        seleccionado = temaSeleccionado == tema,
                        onClick = { temaSeleccionado = tema }
                    )
                    Spacer(modifier = Modifier.height(8.dp))
                }
            }
        },
        confirmButton = {
            TextButton(
                onClick = {
                    alSeleccionar(temaSeleccionado)
                    alCerrar()
                }
            ) {
                Text("Aplicar")
            }
        },
        dismissButton = {
            TextButton(onClick = alCerrar) {
                Text("Cancelar")
            }
        }
    )
}

@Composable
fun ItemTema(
    tema: TemaColor,
    seleccionado: Boolean,
    onClick: () -> Unit
) {
    Surface(
        onClick = onClick,
        modifier = Modifier.fillMaxWidth(),
        shape = RoundedCornerShape(12.dp),
        color = if (seleccionado)
            Color(tema.colorPrimario).copy(alpha = 0.1f)
        else
            MaterialTheme.colorScheme.surfaceVariant,
        border = if (seleccionado) {
            androidx.compose.foundation.BorderStroke(2.dp, Color(tema.colorPrimario))
        } else null
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            verticalAlignment = Alignment.CenterVertically
        ) {
            Box(
                modifier = Modifier
                    .size(40.dp)
                    .clip(CircleShape)
                    .background(Color(tema.colorPrimario)),
                contentAlignment = Alignment.Center
            ) {
                if (seleccionado) {
                    Icon(
                        imageVector = Icons.Default.Check,
                        contentDescription = null,
                        tint = Color.White,
                        modifier = Modifier.size(24.dp)
                    )
                }
            }

            Spacer(modifier = Modifier.width(16.dp))

            Column(modifier = Modifier.weight(1f)) {
                Text(
                    text = tema.nombreMostrar,
                    fontSize = 16.sp,
                    fontWeight = FontWeight.Medium
                )
                Text(
                    text = when(tema) {
                        TemaColor.VERDE -> "El color clásico de SaborForáneo"
                        TemaColor.ROJO -> "Pasión por la cocina"
                        TemaColor.AZUL -> "Fresco y sereno"
                        TemaColor.NARANJA -> "Energético y cálido"
                        TemaColor.MORADO -> "Elegante y sofisticado"
                    },
                    fontSize = 12.sp,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.6f)
                )
            }
        }
    }
}